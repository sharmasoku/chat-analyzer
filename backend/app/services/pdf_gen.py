    # Date Range
    date_min = str(df['date'].min())[:10] if len(df) > 0 else 'N/A'
    date_max = str(df['date'].max())[:10] if len(df) > 0 else 'N/A'
    elements.append(Paragraph(f"Date Range: {date_min} → {date_max}", subtitle_style))
    elements.append(Paragraph(f"Report Generated: {datetime.now().strftime('%B %d, %Y at %I:%M %p')}", subtitle_style))

    elements.append(Spacer(1, 50))
